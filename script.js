    // Create an empty array to store the card HTML
    const cards = [];
    
    // Generate card HTML for each image URL
    for (const url of array) {
        cards.push(generateCard(url));
    }
    
    // Join the card HTML strings into a single string
    const allCardsHtml = cards.join("");
    
    // Append the cards to the body
    conta.innerHTML += allCardsHtml;
